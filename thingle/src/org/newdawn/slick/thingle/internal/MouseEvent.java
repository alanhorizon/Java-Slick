package org.newdawn.slick.thingle.internal;

/**
 * A collection of mouse event identifiers used by Thinlet
 * 
 * @author kevin
 */
public class MouseEvent {
	/** ID for mouse entering */
	public static final int MOUSE_ENTERED = 1;
	/** ID for mouse exited */
	public static final int MOUSE_EXITED = 2;
	/** ID for mouse pressed */
	public static final int MOUSE_PRESSED = 3;
	/** ID for mouse released */
	public static final int MOUSE_RELEASED = 4;
	/** ID for mouse moved */
	public static final int MOUSE_MOVED = 5;
	/** ID for mouse dragged */
	public static final int MOUSE_DRAGGED = 6;
}
